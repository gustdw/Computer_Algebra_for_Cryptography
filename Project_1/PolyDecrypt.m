        Zx := Parent(f);
        cs := [];
        ps := [];
        for i := 1 to num_pairs do
            msg := [Random(0, 1) : j in [1..n]];
            m_poly := Zx ! msg;
            c := PolyEncrypt(msg, f, g, U);
            Append(~cs, c);
            Append(~ps, m_poly);
        end for;
        try
            f_rec, g_rec := PolyPlaintextAttack(cs, ps, n, B, U);
            msg_test := [Random(0, 1) : j in [1..n]];
            c_test := PolyEncrypt(msg_test, f, g, U);
            m_dec := PolyDecrypt(c_test, f_rec, g_rec);
            if m_dec eq msg_test then
                success +:= 1;
            end if;
            if (f_rec eq f or f_rec eq -f or f_rec eq g or f_rec eq -g) and
            (g_rec eq f or g_rec eq -f or g_rec eq g or g_rec eq -g) then
                printf "Keys match up to sign/swap\n";
            elif f_rec ne f or g_rec ne g then
                printf "Recovered keys do not match original keys\n";
            end if;;
        catch e
            ; // attack failed
        end try;
    end for;
    printf "%o/%o\n", success, trials;
end procedure;

